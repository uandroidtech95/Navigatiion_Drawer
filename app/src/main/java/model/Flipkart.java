public class Flipkart {
    private String mobileUrl, mobileName, mobilePrice, mobileOffer;

    public String getMobileUrl() {
        return mobileUrl;
    }

    public void setMobileUrl(String mobileUrl) {
        this.mobileUrl = mobileUrl;
    }

    public String getMobileName() {
        return mobileName;
    }

    public void setMobileName(String mobileName) {
        this.mobileName = mobileName;
    }

    public String getMobilePrice() {
        return mobilePrice;
    }

    public void setMobilePrice(String mobilePrice) {
        this.mobilePrice = mobilePrice;
    }

    public String getMobileOffer() {
        return mobileOffer;
    }

    public void setMobileOffer(String mobileOffer) {
        this.mobileOffer = mobileOffer;
    }
}
