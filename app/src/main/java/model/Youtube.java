public class Youtube {
    private String youtubeUrl, profileUrl, youtubeTitle, youtubeOwner, time, youtubeStream;

    public String getYoutubeUrl() {

        return youtubeUrl;
    }

    public void setYoutubeUrl(String youtubeUrl) {
        this.youtubeUrl = youtubeUrl;
    }

    public String getProfileUrl() {
        return profileUrl;
    }

    public void setProfileUrl(String profileUrl) {
        this.profileUrl = profileUrl;
    }

    public String getYoutubeTitle() {
        return youtubeTitle;
    }

    public void setYoutubeTitle(String youtubeTitle) {
        this.youtubeTitle = youtubeTitle;
    }

    public String getYoutubeOwner() {
        return youtubeOwner;
    }

    public void setYoutubeOwner(String youtubeOwner) {
        this.youtubeOwner = youtubeOwner;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getYoutubeStream() {
        return youtubeStream;
    }

    public void setYoutubeStream(String youtubeStream) {
        this.youtubeStream = youtubeStream;
    }
}
